# write your if/elif/else statements here

if score >= 70:
    print("You got an A!")
elif score >= 60:
    print("You got a B!")
elif score >= 50:
    print("You got a C!")
elif score >= 40:
    print("You got a D!")
else:
    print("You got an F!")
